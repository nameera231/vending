public class Display {
		
	/**
	 * a method that shows that stockpile
	 * runs out of soda
	 * @return a message that indicates that stockpile 
	 * runs out of soda. 
	 */
	public String showSodaSoldOut( ) {
		return "your desired soda is no longer available in stockpile.";
	}	

	/**
	 * a method that shows that soda is
	 * purchased successfully.
	 * @return a message that indicates that soda is 
	 * purchased successfully.
	 */
	public String showSuccessfulPurchase() {
		return "soda purchase completed successfully.";
	}
}
